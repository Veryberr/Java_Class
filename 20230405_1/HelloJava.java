package k20230405;

public class HelloJava {

	public static void main(String[] args) {
		
//		 자동 완성 단축키 : ctrl + space
		
//		 프로그램 실행 단축키 : ctrl + F11
		
//		 주석 : 프로그램에 대한 간단한 설명을 적는다.
//		 한줄 주석 : 앞에 "/"를 연속해서 2개 사용 -> 컴파일러가 번역X
//		 한줄 주석 단축키 : ctrl + /
//		 (주석 유지보수에 용이)
//		 범위 주석 :  "/* ~ */" 사이에 입력한 내용 -> 번역X
//		 범위 주석 단축키 : 블록 지정 후, ctrl + shift + / 그리고 해제 시, ctrl + shift + \
		
		
//		 코드 복사 단축키 : ctrl + alt + ↑(위로 복사), ctrl + alt + ↓(아래로 복사)
		
//		 코드 이동 단축키 : alt + ↑(위로 이동), alt + ↓(아래로 이동)
		
//		println : 괄호 안의 내용 출력 후, 줄 바꿈
//		print : 괄호 안의 내용만 출력
//		printf : 출력 서식 지정해서 출력
		
		System.out.println("안녕 자바1");
		System.out.print("안녕 \n자바2"); // \n : new line => 줄 바꿈
		System.out.println("안녕 자바3");
		
	}
	
}
